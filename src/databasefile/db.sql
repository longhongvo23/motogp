CREATE DATABASE motogp
USE motogp
CREATE TABLE countries (
    country_id INT AUTO_INCREMENT PRIMARY KEY,
    countryName VARCHAR(100) NOT NULL,
    country_code VARCHAR(10),
    flagImg VARCHAR(255)
);
CREATE TABLE circuits (
    circuit_id INT AUTO_INCREMENT PRIMARY KEY,
    circuit_name VARCHAR(100) NOT NULL,
    location VARCHAR(100),
    country_id INT,
    circuit_image_url VARCHAR(255),
    FOREIGN KEY (country_id) REFERENCES countries(country_id)
);
CREATE TABLE events (
    event_id INT AUTO_INCREMENT PRIMARY KEY,
    event_name VARCHAR(100) NOT NULL,
    event_month VARCHAR(50),
    circuit_id INT,
    start_date DATE,
    end_date DATE,
    status ENUM('Finished', 'Upcoming'),
    event_type ENUM('Grands Prix', 'All Event'),
    FOREIGN KEY (circuit_id) REFERENCES circuits(circuit_id)
);
CREATE TABLE categories (
    category_id INT AUTO_INCREMENT PRIMARY KEY,
    category_name VARCHAR(50) NOT NULL
);
CREATE TABLE sessions (
    session_id INT AUTO_INCREMENT PRIMARY KEY,
    session_name VARCHAR(100)
);
CREATE TABLE teams (
    team_id INT AUTO_INCREMENT PRIMARY KEY,
    backgroundName VARCHAR(100) NOT NULL,
    team_type VARCHAR(50) NOT NULL,
    teamName VARCHAR(100) NOT NULL,
    motoImg VARCHAR(255),
    riderName1 VARCHAR(255),
    riderName2 VARCHAR(255)
);
CREATE TABLE riders (
    rider_id INT AUTO_INCREMENT PRIMARY KEY,
    rider_type VARCHAR(50) NOT NULL,
    backgroundName VARCHAR(5) NOT NULL,
    riderImg VARCHAR(255),
    riderHashtag VARCHAR(100) NOT NULL,
    firstname VARCHAR(100) NOT NULL,
    lastname VARCHAR(100) NOT NULL,
    country_id INT,
    team_id INT,
    rider_number INT,
    FOREIGN KEY (country_id) REFERENCES countries(country_id),
    FOREIGN KEY (team_id) REFERENCES teams(team_id)
);
CREATE TABLE rider_statistics (
    statistic_id INT AUTO_INCREMENT PRIMARY KEY,
    rider_id INT,
    season_year INT,
    currentPos INT,
    currentPoint INT,
    currentVic INT,
    totalWorldCham INT,
    totalVic INT,
    totalPod INT,
    totalPole INT,
    totalRace INT,
    FOREIGN KEY (rider_id) REFERENCES riders(rider_id)
);
CREATE TABLE results (
    result_id INT AUTO_INCREMENT PRIMARY KEY,
	event_id INT,
    category_id INT,
    session_id INT,
    rider_id INT,
    team_id INT,
    position INT,
    points INT,
    time_gap VARCHAR(50),
    FOREIGN KEY (session_id) REFERENCES sessions(session_id),
    FOREIGN KEY (rider_id) REFERENCES riders(rider_id),
    FOREIGN KEY (team_id) REFERENCES teams(team_id),
    FOREIGN KEY (event_id) REFERENCES events(event_id),
    FOREIGN KEY (category_id) REFERENCES categories(category_id)
);
CREATE TABLE team_statistics (
    statistic_id INT AUTO_INCREMENT PRIMARY KEY,
    team_id INT,
    season_year INT,
    position INT,
    points INT,
    FOREIGN KEY (team_id) REFERENCES teams(team_id)
);
